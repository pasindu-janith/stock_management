    private void loadCompany() {
        try {
            ResultSet rs = DB.search("SELECT `company_id`,`company_name` FROM company WHERE `status`='1'");
            Vector v = new Vector();
            while (rs.next()) {
                v.add(rs.getString("company_id") + "-" + rs.getString("company_name"));
            }
            combo_company.setModel(new DefaultComboBoxModel<>(v));
            combo_company.setSelectedIndex(0);
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

    private void clearFields() {
        txt_branchName.setText(null);
        txt_contactno.setText(null);
        txt_addline1.setText(null);
        txt_addline2.setText(null);
        txt_addline3.setText(null);
        combo_company.setSelectedIndex(0);
    }
