    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        if (!txt_branchName.getText().equals("") && !txt_contactno.getText().equals("")
                && !txt_addline1.getText().equals("") && !txt_addline2.getText().equals("")) {
            try {
                DB.push("INSERT INTO address(`address_line1`,`address_line2`,`address_line3`) VALUES('"
                        + txt_addline1.getText().trim() + "','"
                        + txt_addline2.getText().trim() + "','"
                        + txt_addline3.getText().trim() + "')");
                ResultSet rs = DB.search("SELECT `id` FROM address WHERE `address_line1`='"
                        + txt_addline1.getText() + "' AND `address_line2`='"
                        + txt_addline2.getText() + "' AND `address_line3`='"
                        + txt_addline3.getText() + "'");
                if (rs.next()) {
                    DB.push("INSERT INTO branch(`company_id`,`branch_name`,`branch_contactno`,`address_id`)"
                            + " VALUES('" + combo_company.getSelectedItem().toString().split("-")[0] + "','"
                            + txt_branchName.getText() + "','" + txt_contactno.getText() + "','"
                            + rs.getString("id") + "')");
                    JOptionPane.showMessageDialog(this, "Branch registered!");
                    clearFields();
                }
            } catch (Exception e) {
                e.printStackTrace();
            }
        } else {
            JOptionPane.showMessageDialog(this, "Fill all mandatory fields!");
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void combo_companyActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_combo_companyActionPerformed
        try {
            ResultSet rs = DB.search("SELECT `branch_id`,`branch_name` FROM branch WHERE `company_id`='"
                    + combo_company.getSelectedItem().toString().split("-")[0] + "' AND `status`='1'");
            Vector v = new Vector();
            while (rs.next()) {
                v.add(rs.getString("branch_id") + "-" + rs.getString("branch_name"));
            }
            combo_branch.setModel(new DefaultComboBoxModel<>(v));
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_combo_companyActionPerformed

    private void combo_branchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_combo_branchActionPerformed
        try {
            ResultSet rs = DB.search("SELECT * FROM branch b INNER JOIN address a ON a.`id`=b.`address_id`"
                    + " WHERE `branch_id`='" + combo_branch.getSelectedItem().toString().split("-")[0] + "'");
            if (rs.next()) {
                txt_branchName.setText(rs.getString("branch_name"));
                txt_branchName.setText(rs.getString("branch_contactno"));
                txt_addline1.setText(rs.getString("address_line1"));
                txt_addline2.setText(rs.getString("address_line2"));
                txt_addline3.setText(rs.getString("address_line3"));
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_combo_branchActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            ResultSet rs = DB.search("SELECT `address_id` FROM branch WHERE `branch_id`='"
                    + combo_branch.getSelectedItem().toString().split("-")[0] + "'");
            if (rs.next()) {
                DB.push("UPDATE address SET `address_line1`='"
                        + txt_addline1.getText() + "',`address_line2`='"
                        + txt_addline2.getText() + "',`address_line3`='"
                        + txt_addline3.getText() + "' WHERE `id`='"
                        + rs.getString("address_id") + "'");
            }
            int status = (rad_active.isSelected()) ? 1 : 0;
            DB.push("UPDATE branch SET `branch_name`='"
                    + txt_branchName.getText() + "',`branch_contactno`='"
                    + txt_contactno.getText() + "',`status`='" + status + "' WHERE `branch_id`='"
                    + combo_branch.getSelectedItem().toString().split("-")[0] + "'");
            JOptionPane.showMessageDialog(this, "Branch Updated!");
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_jButton1ActionPerformed


