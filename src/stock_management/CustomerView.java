    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            if (txt_nic.getText() != null && !txt_nic.equals("")
                    && txt_fname.getText() != null && !txt_fname.getText().equals("")
                    && txt_lname.getText() != null && !txt_lname.getText().equals("")
                    && txt_contactno.getText() != null && !txt_contactno.getText().equals("")
                    && txt_address1.getText() != null && !txt_address1.getText().equals("")
                    && txt_address2.getText() != null && !txt_address2.getText().equals("")) {
                String gender = (rad_male.isSelected()) ? "MALE" : "FEMALE";
                int status = 1;
                if (rad_inactive.isSelected()) {
                    status = 0;
                }
                DB.push("INSERT INTO address(`address_line1`,`address_line2`,`address_line3`) VALUES('"
                        + txt_address1.getText() + "','"
                        + txt_address2.getText() + "','"
                        + txt_address3.getText() + "')");
                ResultSet rs = DB.search("SELECT id FROM address WHERE `address_line1`='"
                        + txt_address1.getText() + "' AND `address_line2`='"
                        + txt_address2.getText() + "' AND `address_line3`='"
                        + txt_address3.getText() + "'");
                if (rs.next()) {
                    DB.push("INSERT INTO customer VALUES('"
                            + txt_nic.getText() + "','"
                            + txt_fname.getText() + "','"
                            + txt_lname.getText() + "','"
                            + txt_contactno.getText() + "','"
                            + gender + "','" + rs.getInt("id") + "','" + status + "')");
                    JOptionPane.showMessageDialog(this, "Customer Registered sucessfully!");
                    clearFields();
                }
            } else {
                JOptionPane.showMessageDialog(this, "Fill All mandatory fields!");
            }
        } catch (java.sql.SQLIntegrityConstraintViolationException e) {
            JOptionPane.showMessageDialog(this, "This Customer already Registered!");
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        try {
            if (txt_nic.getText() != null && !txt_nic.equals("")
                    && txt_fname.getText() != null && !txt_fname.getText().equals("")
                    && txt_lname.getText() != null && !txt_lname.getText().equals("")
                    && txt_contactno.getText() != null && !txt_contactno.getText().equals("")
                    && txt_address1.getText() != null && !txt_address1.getText().equals("")
                    && txt_address2.getText() != null && !txt_address2.getText().equals("")) {
                String gender = (rad_male.isSelected()) ? "MALE" : "FEMALE";
                int status = 1;
                if (rad_inactive.isSelected()) {
                    status = 0;
                }
                ResultSet rs = DB.search("SELECT `address` FROM customer WHERE `nic`='" + txt_nic.getText() + "'");
                if (rs.next()) {
                    DB.push("UPDATE address SET `address_line1`='"
                            + txt_address1.getText() + "',`address_line2`='"
                            + txt_address2.getText() + "',`address_line3`='"
                            + txt_address3.getText() + "' WHERE `id`='" + rs.getInt("address") + "'");

                    DB.push("UPDATE customer SET `fname`='"
                            + txt_fname.getText() + "',`lname`='"
                            + txt_lname.getText() + "',`contactno`='"
                            + txt_contactno.getText() + "',`status`='"
                            + status + "',`gender`='"
                            + gender + "' WHERE `nic`='" + txt_nic.getText() + "'");
                    JOptionPane.showMessageDialog(this, "Customer Updated sucessfully!");
                    clearFields();

                } else {
                    JOptionPane.showMessageDialog(this, "No registered customer from this NIC to update");
                }
            } else {
                JOptionPane.showMessageDialog(this, "Fill All mandatory fields!");
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_jButton2ActionPerformed

