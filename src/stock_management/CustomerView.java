    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        try {
            if (!txt_nic.getText().equals("") && txt_nic.getText() != null && !txt_nic.getText().equals("C1")) {
                ResultSet rs = DB.search("SELECT * FROM customer c INNER JOIN address a ON a.`id`=c.`address`"
                        + " WHERE `nic`='" + txt_nic.getText() + "'");
                if (rs.next()) {
                    txt_fname.setText(rs.getString("fname"));
                    txt_lname.setText(rs.getString("lname"));
                    txt_contactno.setText(rs.getString("contactno"));
                    if (rs.getString("gender").equals("MALE")) {
                        rad_male.setSelected(true);
                    } else {
                        rad_female.setSelected(true);
                    }
                    if (rs.getString("status").equals("1")) {
                        rad_active.setSelected(true);
                    } else {
                        rad_inactive.setSelected(true);
                    }
                    txt_address1.setText(rs.getString("address_line1"));
                    txt_address2.setText(rs.getString("address_line2"));
                    txt_address3.setText(rs.getString("address_line3"));

                } else {
                    JOptionPane.showMessageDialog(this, "No Registered customer from this NIC");
                }
            } else {
                JOptionPane.showMessageDialog(this, "Enter the customer's NIC to search");
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_jButton4ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        try {
            if (!txt_nic.getText().equals("") && txt_nic.getText() != null) {
                int confirmDialog = JOptionPane.showConfirmDialog(this, "Are you sure to delete customer!",
                        "Delete Customer", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);
                if (confirmDialog == JOptionPane.YES_OPTION) {
                    DB.push("UPDATE customer SET `status`='0' WHERE `nic`='" + txt_nic.getText() + "'");
                    JOptionPane.showMessageDialog(this, "Customer Deleted!");
                }
            }
        } catch (Exception e) {
            e.printStackTrace();
        }

    }//GEN-LAST:event_jButton3ActionPerformed

