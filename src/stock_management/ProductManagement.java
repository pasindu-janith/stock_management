    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        try {
            if (txt_name.getText() != null && !txt_name.getText().trim().equals("")
                    && txt_code.getText() != null && !txt_code.getText().trim().equals("")
                    && txt_buyprice.getText() != null && !txt_buyprice.getText().equals("")
                    && txt_sellprice.getText() != null && !txt_sellprice.getText().equals("")
                    && !combo_brand.getSelectedItem().toString().equals("")) {

                String brand = combo_brand.getSelectedItem().toString().split("-")[0];
                String category = combo_category.getSelectedItem().toString().split("-")[0];
                int status = (rad_active.isSelected()) ? 1 : 0;
                try {
                    DB.push("INSERT INTO product VALUES('"
                            + txt_code.getText() + "','"
                            + FormatText.nameFormat(txt_name.getText()) + "','"
                            + brand + "','"
                            + category + "','"
                            + txt_buyprice.getText() + "','"
                            + txt_sellprice.getText() + "','"
                            + txt_weight.getText() + "','"
                            + txt_description.getText().trim() + "','"
                            + imagePath + "','" + status + "')");

                    JOptionPane.showMessageDialog(this, "Product Added!");
                    clearFields();
                    imagePath = null;
                } catch (java.sql.SQLIntegrityConstraintViolationException e) {
                    JOptionPane.showMessageDialog(this, "This product already Registered!");
                }

            } else {
                JOptionPane.showMessageDialog(this, "Fill all mandatory fields!");
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_jButton2ActionPerformed

