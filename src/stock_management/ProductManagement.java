    private void loadBrands(String search_brand) {
        try {
            ResultSet rs = DB.search("SELECT * FROM brand WHERE `name` LIKE '"
                    + search_brand + "%' AND `status`='1'");
            Vector v = new Vector();

            while (rs.next()) {
                String brand = rs.getString("id") + "-" + rs.getString("name");
                v.add(brand);
            }
            combo_brand.setModel(new DefaultComboBoxModel(v));
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

    private void loadCategory() {
        try {
            ResultSet rs = DB.search("SELECT * FROM category");
            Vector v = new Vector();
            while (rs.next()) {
                String category = rs.getString("id") + "-" + rs.getString("category");
                v.add(category);
            }
            combo_category.setModel(new DefaultComboBoxModel(v));
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

