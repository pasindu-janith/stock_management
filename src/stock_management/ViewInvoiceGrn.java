    private void txt_invoiceidActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txt_invoiceidActionPerformed
        try {
            if(!txt_invoiceid.getText().equals("")){
                ResultSet rs = DB.search("SELECT * FROM invoice WHERE `invoiceno`='"+txt_invoiceid.getText()+"'");
                if(rs.next()){
                    double nettotal = rs.getDouble("nettotal");
                    double payment = rs.getDouble("payment");
                    txt_customerid.setText(rs.getString("customer"));
                    txt_invoicedate.setText(rs.getString("datetime"));
                    txt_invemployee.setText(rs.getString("employee"));
                    txt_invtotal.setText(""+rs.getDouble("total"));
                    txt_discount.setText(""+rs.getDouble("discount"));
                    txt_invnettotal.setText(""+nettotal);
                    txt_payment.setText(""+payment);
                    txt_balance.setText(""+(payment - nettotal));
                    ResultSet rs_search = DB.search("SELECT `fname`,`lname` FROM customer WHERE `nic`='"
                            +rs.getString("customer")+"'");
                    if(rs_search.next()){
                        txt_customername.setText(rs_search.getString("fname")+" "+rs_search.getString("lname"));
                    }
                    DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
                    dtm.setRowCount(0);
                    rs_search = DB.search("SELECT * FROM invoice_item WHERE invoiceno='"+txt_invoiceid.getText()+"'");
                    while(rs_search.next()){
                        Vector v = new Vector();
                        ResultSet rs_product = DB.search("SELECT p.`name`,p.`weight`,p.`brand`,b.`name`"
                                + " FROM product p INNER JOIN brand b ON p.`brand` = b.`id` WHERE p.`code`='"
                                +rs_search.getString("productcode")+"'");
                        if(rs_product.next()){
                            String productname = rs_product.getString("name")+" "+rs_product.getDouble("weight")+"g";
                            if(rs_product.getInt("brand") != 1){
                                productname = rs_product.getString("b.name")+" "+productname;
                            }
                            v.add(rs_search.getString("productcode"));
                            v.add(productname);
                            v.add(rs_search.getDouble("qty"));
                            v.add(rs_search.getDouble("sellprice"));
                            v.add(rs_search.getDouble("subtotal"));
                        }
                            dtm.addRow(v);
                    }
                } else{
                    JOptionPane.showMessageDialog(this, "No Invoice Issued from this ID");
                }
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_txt_invoiceidActionPerformed

