    private void txt_ponoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txt_ponoActionPerformed
        try {
            if(!txt_pono.getText().equals("")){
                ResultSet rs = DB.search("SELECT * FROM PO WHERE `POno`='"+txt_pono.getText()+"'");
                if(rs.next()){
                    txt_podate.setText(rs.getString("POdate"));
                    txt_posupplierid.setText(rs.getString("supplier"));
                    ResultSet rs_sup = DB.search("SELECT s.`fname`,s.`lname`,b.`name` "
                            + "FROM supplier s INNER JOIN brand b ON s.`brand` = b.`id` WHERE `nic`='"
                            +rs.getString("supplier")+"'");
                    if(rs_sup.next()){
                        txt_posuppliername.setText(rs_sup.getString("fname")+" "+rs_sup.getString("lname")+"-"+rs_sup.getString("name"));
                    }
                    String status = (rs.getInt("status") == 1) ? "ACTIVE" : "INACTIVE";
                    txt_postat.setText(status);
                    txt_deleverydate.setText(rs.getString("deliverydate"));
                    rs = DB.search("SELECT * FROM POitem WHERE `POno`='"+txt_pono.getText()+"'");
                    while(rs.next()){
                        DefaultTableModel dtm = (DefaultTableModel) jTable3.getModel();
                        Vector v = new Vector();
                        dtm.setRowCount(0);
                        ResultSet rs_item = DB.search("SELECT * FROM product p INNER JOIN brand b ON p.`brand`=b.`id`"
                                + " WHERE `code`='"+rs.getString("productcode")+"'");
                        if(rs_item.next()){
                            double buyprice = rs_item.getDouble("buyprice");
                            double qty = rs.getDouble("qty");
                            String productname = rs_item.getString("b.name")+" "+rs_item.getString("p.name")
                                    +" "+rs_item.getDouble("weight")+"g";
                            v.add(rs.getString("productcode"));
                            v.add(productname);
                            v.add(buyprice);
                            v.add(qty);
                            v.add(buyprice * qty);
                        }
                        dtm.addRow(v);
                    }
                } else{
                    JOptionPane.showMessageDialog(this, "No active PO from this PO-NO");
                    txt_pono.setText(null);
                    txt_pono.grabFocus();
                }
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_txt_ponoActionPerformed

